#!/bin/bash

function createNsporg() {
  echo "Enrolling the CA admin"
  mkdir -p organizations/peerOrganizations/nsporg.isp.com/

  export FABRIC_CA_CLIENT_HOME=${PWD}/organizations/peerOrganizations/nsporg.isp.com/

  set -x
  fabric-ca-client enroll -u https://admin:adminpw@localhost:6054 --caname ca-nsporg --tls.certfiles "${PWD}/organizations/fabric-ca/nsporg/ca-cert.pem"
  { set +x; } 2>/dev/null

  echo 'NodeOUs:
  Enable: true
  ClientOUIdentifier:
    Certificate: cacerts/localhost-6054-ca-nsporg.pem
    OrganizationalUnitIdentifier: client
  PeerOUIdentifier:
    Certificate: cacerts/localhost-6054-ca-nsporg.pem
    OrganizationalUnitIdentifier: peer
  AdminOUIdentifier:
    Certificate: cacerts/localhost-6054-ca-nsporg.pem
    OrganizationalUnitIdentifier: admin
  OrdererOUIdentifier:
    Certificate: cacerts/localhost-6054-ca-nsporg.pem
    OrganizationalUnitIdentifier: orderer' > "${PWD}/organizations/peerOrganizations/nsporg.isp.com/msp/config.yaml"

  # Since the CA serves as both the organization CA and TLS CA, copy the org's root cert that was generated by CA startup into the org level ca and tlsca directories

  # Copy nsporg's CA cert to nsporg's /msp/tlscacerts directory (for use in the channel MSP definition)
  mkdir -p "${PWD}/organizations/peerOrganizations/nsporg.isp.com/msp/tlscacerts"
  cp "${PWD}/organizations/fabric-ca/nsporg/ca-cert.pem" "${PWD}/organizations/peerOrganizations/nsporg.isp.com/msp/tlscacerts/ca.crt"

  # Copy nsporg's CA cert to nsporg's /tlsca directory (for use by clients)
  mkdir -p "${PWD}/organizations/peerOrganizations/nsporg.isp.com/tlsca"
  cp "${PWD}/organizations/fabric-ca/nsporg/ca-cert.pem" "${PWD}/organizations/peerOrganizations/nsporg.isp.com/tlsca/tlsca.nsporg.isp.com-cert.pem"

  # Copy nsporg's CA cert to nsporg's /ca directory (for use by clients)
  mkdir -p "${PWD}/organizations/peerOrganizations/nsporg.isp.com/ca"
  cp "${PWD}/organizations/fabric-ca/nsporg/ca-cert.pem" "${PWD}/organizations/peerOrganizations/nsporg.isp.com/ca/ca.nsporg.isp.com-cert.pem"

  echo "Registering peer0"
  set -x
  fabric-ca-client register --caname ca-nsporg --id.name peer0 --id.secret peer0pw --id.type peer --tls.certfiles "${PWD}/organizations/fabric-ca/nsporg/ca-cert.pem"
  { set +x; } 2>/dev/null

  echo "Registering user"
  set -x
  fabric-ca-client register --caname ca-nsporg --id.name user1 --id.secret user1pw --id.type client --tls.certfiles "${PWD}/organizations/fabric-ca/nsporg/ca-cert.pem"
  { set +x; } 2>/dev/null

  echo "Registering the org admin"
  set -x
  fabric-ca-client register --caname ca-nsporg --id.name nsporgadmin --id.secret nsporgadminpw --id.type admin --tls.certfiles "${PWD}/organizations/fabric-ca/nsporg/ca-cert.pem"
  { set +x; } 2>/dev/null

  echo "Generating the peer0 msp"
  set -x
  fabric-ca-client enroll -u https://peer0:peer0pw@localhost:6054 --caname ca-nsporg -M "${PWD}/organizations/peerOrganizations/nsporg.isp.com/peers/peer0.nsporg.isp.com/msp" --tls.certfiles "${PWD}/organizations/fabric-ca/nsporg/ca-cert.pem"
  { set +x; } 2>/dev/null

  cp "${PWD}/organizations/peerOrganizations/nsporg.isp.com/msp/config.yaml" "${PWD}/organizations/peerOrganizations/nsporg.isp.com/peers/peer0.nsporg.isp.com/msp/config.yaml"

  echo "Generating the peer0-tls certificates, use --csr.hosts to specify Subject Alternative Names"
  set -x
  fabric-ca-client enroll -u https://peer0:peer0pw@localhost:6054 --caname ca-nsporg -M "${PWD}/organizations/peerOrganizations/nsporg.isp.com/peers/peer0.nsporg.isp.com/tls" --enrollment.profile tls --csr.hosts peer0.nsporg.isp.com --csr.hosts localhost --tls.certfiles "${PWD}/organizations/fabric-ca/nsporg/ca-cert.pem"
  { set +x; } 2>/dev/null

  # Copy the tls CA cert, server cert, server keystore to well known file names in the peer's tls directory that are referenced by peer startup config
  cp "${PWD}/organizations/peerOrganizations/nsporg.isp.com/peers/peer0.nsporg.isp.com/tls/tlscacerts/"* "${PWD}/organizations/peerOrganizations/nsporg.isp.com/peers/peer0.nsporg.isp.com/tls/ca.crt"
  cp "${PWD}/organizations/peerOrganizations/nsporg.isp.com/peers/peer0.nsporg.isp.com/tls/signcerts/"* "${PWD}/organizations/peerOrganizations/nsporg.isp.com/peers/peer0.nsporg.isp.com/tls/server.crt"
  cp "${PWD}/organizations/peerOrganizations/nsporg.isp.com/peers/peer0.nsporg.isp.com/tls/keystore/"* "${PWD}/organizations/peerOrganizations/nsporg.isp.com/peers/peer0.nsporg.isp.com/tls/server.key"

  echo "Generating the user msp"
  set -x
  fabric-ca-client enroll -u https://user1:user1pw@localhost:6054 --caname ca-nsporg -M "${PWD}/organizations/peerOrganizations/nsporg.isp.com/users/User1@nsporg.isp.com/msp" --tls.certfiles "${PWD}/organizations/fabric-ca/nsporg/ca-cert.pem"
  { set +x; } 2>/dev/null

  cp "${PWD}/organizations/peerOrganizations/nsporg.isp.com/msp/config.yaml" "${PWD}/organizations/peerOrganizations/nsporg.isp.com/users/User1@nsporg.isp.com/msp/config.yaml"

  echo "Generating the org admin msp"
  set -x
  fabric-ca-client enroll -u https://nsporgadmin:nsporgadminpw@localhost:6054 --caname ca-nsporg -M "${PWD}/organizations/peerOrganizations/nsporg.isp.com/users/Admin@nsporg.isp.com/msp" --tls.certfiles "${PWD}/organizations/fabric-ca/nsporg/ca-cert.pem"
  { set +x; } 2>/dev/null

  cp "${PWD}/organizations/peerOrganizations/nsporg.isp.com/msp/config.yaml" "${PWD}/organizations/peerOrganizations/nsporg.isp.com/users/Admin@nsporg.isp.com/msp/config.yaml"
}

