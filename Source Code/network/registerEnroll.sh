    cp "${PWD}/organizations/ordererOrganizations/isp.com/orderers/${ORDERER}.isp.com/tls/keystore/"* "${PWD}/organizations/ordererOrganizations/isp.com/orderers/${ORDERER}.isp.com/tls/server.key"

