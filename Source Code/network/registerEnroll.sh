    mkdir -p "${PWD}/organizations/ordererOrganizations/isp.com/orderers/${ORDERER}.isp.com/msp/tlscacerts"
    cp "${PWD}/organizations/ordererOrganizations/isp.com/orderers/${ORDERER}.isp.com/tls/tlscacerts/"* "${PWD}/organizations/ordererOrganizations/isp.com/orderers/${ORDERER}.isp.com/msp/tlscacerts/tlsca.isp.com-cert.pem"
